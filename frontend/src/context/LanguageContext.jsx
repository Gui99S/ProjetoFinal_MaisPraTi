export function LanguageProvider({ children }) {
  const [currentLanguage, setCurrentLanguage] = useState(() => {
    // Try to get saved language from localStorage, default to 'en'
    return localStorage.getItem('language') || 'en';
  });

  // Save language preference to localStorage when it changes
  useEffect(() => {
    localStorage.setItem('language', currentLanguage);
  }, [currentLanguage]);

  const changeLanguage = (language) => {
    if (translations[language]) {
      setCurrentLanguage(language);
    }
  };

  const translate = (key, params = {}) => {
    let translation = translations[currentLanguage][key] || key;
    
    // Replace placeholders like {name}, {page}, {total}, etc.
    Object.keys(params).forEach(param => {
      translation = translation.replace(new RegExp(`\\{${param}\\}`, 'g'), params[param]);
    });
    
    return translation;
  };

  const value = {
    currentLanguage,
    changeLanguage,
    translate,
    availableLanguages: [
      { code: 'en', name: 'English' },
      { code: 'pt', name: 'Portugu√™s' },
      { code: 'es', name: 'Espa√±ol' }
    ]
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    // Fallback to a no-op implementation to avoid runtime crashes when
    // components call useLanguage() outside of the provider (helps during
    // development). Log a warning so the issue can be tracked down.
    // Prefer fixing the provider placement in the app root, but this
    // defensive fallback keeps the UI usable.
    // eslint-disable-next-line no-console
    console.warn('useLanguage() was called outside of a LanguageProvider ‚Äî returning fallback.');
    return {
      currentLanguage: 'en',
      changeLanguage: () => {},
      translate: (key) => key,
      availableLanguages: [
        { code: 'en', name: 'English' },
        { code: 'pt', name: 'Portugu√™s' },
        { code: 'es', name: 'Espa√±ol' }
      ]
    };
  }

  return context;
}